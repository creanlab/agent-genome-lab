#!/usr/bin/env node
/**
 * nve-export-dashboard.js — Generate web/data.js for offline dashboard use.
 *
 * Usage:
 *   node cli/nve-export-dashboard.js
 *
 * Reads .evolution/ data and generates web/data.js with embedded JSON.
 * After running this, web/index.html can be opened as file:/// without server.
 */
const fs = require('fs');
const path = require('path');

const ROOT = (() => {
  let dir = process.cwd();
  for (let i = 0; i < 10; i++) {
    if (fs.existsSync(path.join(dir, '.evolution'))) return dir;
    const p = path.dirname(dir);
    if (p === dir) break;
    dir = p;
  }
  return process.cwd();
})();
const EVO = path.join(ROOT, '.evolution');

function loadJsonDir(subdir) {
  const dir = path.join(EVO, subdir);
  if (!fs.existsSync(dir)) return [];
  return fs.readdirSync(dir)
    .filter(f => f.endsWith('.json') && !f.startsWith('.'))
    .sort()
    .map(f => {
      try { return JSON.parse(fs.readFileSync(path.join(dir, f), 'utf8')); }
      catch { return null; }
    })
    .filter(Boolean);
}

// Build embedded data
const incidents = loadJsonDir('incidents');
const units = loadJsonDir('experience_units');
const genomes = loadJsonDir('failure_genomes');
const audits = loadJsonDir('audits');

// Load manifest
const manifestPath = path.join(EVO, 'repo-manifest.json');
const manifest = fs.existsSync(manifestPath)
  ? JSON.parse(fs.readFileSync(manifestPath, 'utf8'))
  : null;

// Load journal for legacy dashboard sections
const journalPath = path.join(EVO, 'evolution_journal.md');
const journal = fs.existsSync(journalPath)
  ? fs.readFileSync(journalPath, 'utf8')
  : '';

const data = {
  _generated: new Date().toISOString(),
  _generator: 'nve-export-dashboard.js',
  _offline: true,
  incidents,
  experience_units: units,
  failure_genomes: genomes.filter(g => !g.index_version), // exclude FAMILY_INDEX
  family_index: genomes.find(g => g.index_version) || null,
  audits,
  manifest,
  journal,
};

const webDir = path.join(ROOT, 'web');
if (!fs.existsSync(webDir)) fs.mkdirSync(webDir, { recursive: true });

const dataPath = path.join(webDir, 'data.js');
const content = `// Auto-generated by nve-export-dashboard.js — ${new Date().toISOString()}
// Open web/index.html as file:/// to use offline dashboard
window.__NVE_OFFLINE_DATA__ = ${JSON.stringify(data, null, 2)};
`;

fs.writeFileSync(dataPath, content);

console.log(`
📦 Offline dashboard data exported
   File:      ${dataPath}
   Incidents: ${incidents.length}
   EUs:       ${units.length}
   Genomes:   ${genomes.filter(g => !g.index_version).length}
   Audits:    ${audits.length}

   Now open web/index.html as file:/// — no server needed.
`);
